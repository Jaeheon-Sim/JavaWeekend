package com.kita.first.level1;

public class Switch {
	public static void main(String[] args) {
//		\\192.168.3.254
		int num =1;
		switch(num) {//문자열 비교
		case 1 :
			System.out.println("1");
			break;
		case 2 :
			System.out.println("2");
			break;
		default :
			System.out.println("0");
		}
	}
}
